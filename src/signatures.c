	if (!geneticSignature)
		{
		fprintf (stderr, "Could not allocate the geneticSignature array\n");
		exit (-1);
		}
	for (l=0; l<NUM_SIGNATURES; l++)
		{
		geneticSignature[l] = (double***) calloc (4, sizeof(double**));
		if (!geneticSignature[l])
			{
			fprintf (stderr, "Could not allocate the geneticSignature[%d] array\n", l);
			exit (-1);
			}
		for (i=0; i<4; i++)
			{
			geneticSignature[l][i] = (double**) calloc (6, sizeof(double*));
			if (!geneticSignature[l][i])
				{
				fprintf (stderr, "Could not allocate the geneticSignature[%d][%d] array\n", l, i);
				exit (-1);
				}
			for (j=0; j<6; j++)
				{
				geneticSignature[l][i][j] = (double*) calloc (4, sizeof(double));
				if (!geneticSignature[l][i][j])
					{
					fprintf (stderr, "Could not allocate the geneticSignature[%d][%d][%d] array\n", l, i, j);
					exit (-1);
					}
				}
			}
		}

	/* allocate matrix to store the signatures in 2D */
	signatureProbs = (double**) calloc (NUM_SIGNATURES, sizeof(double));
	if (!signatureProbs)
		{
		fprintf (stderr, "Could not allocate the signatureProbs array\n");
		exit (-1);
		}
